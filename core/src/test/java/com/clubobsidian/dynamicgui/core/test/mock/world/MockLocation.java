package com.clubobsidian.dynamicgui.core.test.mock.world;

import com.clubobsidian.dynamicgui.core.world.WorldWrapper;

public class MockLocation {

    private final int x;
    private final int y;
    private final int z;
    private final WorldWrapper<?> world;

    public MockLocation(int x, int y, int z, WorldWrapper<?> world) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.world = world;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getZ() {
        return this.z;
    }

    public WorldWrapper<?> getWorld() {
        return this.world;
    }
}
